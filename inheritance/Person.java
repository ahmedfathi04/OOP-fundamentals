
public class Person {
    String name ;
    int age ;
    int NID ;

    public Person(String N, int A, int NI ) {
        name = N ; 
        age = A ;
        NID = NI ; 

        
    }
    
    
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
    public int getNID() {
        return NID;
    }



}
